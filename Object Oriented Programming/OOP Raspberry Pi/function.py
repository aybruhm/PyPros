# Functions allow us to give a name to a set of instructions. 
# You can pass data to a function, 
# and optionally you can have it return some data as a result.

def tell(intro, punchline):
	print("Knock! Knock!!")
	print("Who's there?")
	print(intro)
	print(intro + ", who?")
	print(punchline)

tell("Israel", "Sounds like you've got a cold!")